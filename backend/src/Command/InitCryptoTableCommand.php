    public function __construct(EntityManagerInterface $em)
    {
        $this->em = $em;
        parent::__construct();
    }

    protected function configure()
    {
        $this->setDescription(self::$defaultDescription);
    }

    protected function execute(InputInterface $input, OutputInterface $output): int
    {
        $io = new SymfonyStyle($input, $output);

        foreach ($this->cryptolist as $currentCrypto) {

            $crypto = new Crypto();
            $crypto->setPairName($currentCrypto[0]);
            $crypto->setSymbol($currentCrypto[1]);
            $crypto->setName($currentCrypto[2]);
            $crypto->setImageUrl($currentCrypto[3]);

            $this->em->persist($crypto);
        }

        $this->em->flush();

        $io->success('table Crypto alimentée');

        return Command::SUCCESS;
    }
